/*
    printf문을 이용하여 다음과 같은 결과가 나올 수 있도록 프로그램을 작성하라.
    
    즐거운 "C" 프로그래밍 실습!!
    10 % 2 = 0입니다.
    \t는 수평 탭, \n은 개행, \0은 널문자입니다.
    ASCII code에서 'A'는 65입니다.
*/
#include "stdio.h"
int main(void){
	printf("즐거운 \"C\"프로그래밍 실습!!\n");
	printf("10 %% 2 = 0입니다.\n");
	printf("\\t는 수평 탭, \\n은 개행, \\0은 널문자입니다.\n");
	printf("ASCII code에서 'A'는 65입니다");
	return 0;
}

