/*
    ls는 어떤 명령인가?
    ls 명령어의 man page를 읽고 각 명령에 대해 간략하게 설명하고 주어진 옵션에 대해 설명하여라.
    
    ls : 파일들을 리스트로 보여줌
    -l : 파일에 대한 정보를 자세하게 보여줌
    -a : .으로 시작하는 목록을 모두 출력
    -d : 자신이 가진 디렉토리를 보여줌
    -i : 각 파일의 inode 번호를 출력
    -1 : 한줄에 한 파일씩 출력
    
    ls -l 결과에서 접근권한 뒤의 +의 의미는?
    : 일반적인 POSIX 권한 시스템의 확장인 ACL(Access Control List)의 존재를 의미
*/